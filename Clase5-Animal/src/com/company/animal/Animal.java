package com.company.animal;

public class Animal {
    // Variables
    int cantidadDePatas;
    boolean isAmistoso;

    // Getters y Setters
    public int getCantidadDePatas() {
        return cantidadDePatas;
    }

    public void setCantidadDePatas(int cantidadDePatas) {
        this.cantidadDePatas = cantidadDePatas;
    }

    public boolean isAmistoso() {
        return isAmistoso;
    }

    public void setAmistoso(boolean amistoso) {
        isAmistoso = amistoso;
    }
}
